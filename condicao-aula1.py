print((10 > 5) and (2 == 2)) # true and true -> True
print((10 < 5) and (2 == 2)) # false and true -> False

print((10 < 5) or (2 == 2)) # False and True -> True
print((10 > 5) or (2 == 3)) # True and False -> True

print(not (10 == 10)) # Not True -> False